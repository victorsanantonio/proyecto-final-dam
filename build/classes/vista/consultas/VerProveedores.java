    private void eliminar(){
        //Si se ha seleccionado un elemento de la lista
        int opcion = JOptionPane.showConfirmDialog(null,
                "¿Estás seguro de que deseas borrar el registro? Los cambios serán irreversibles", "Confirmación de borrado", JOptionPane.YES_NO_OPTION);
        //Si decide borrar
        if (opcion == 0) {
            borrados.eliminarProveedor(extraerId());

            //Actualizar lista
            dlm = consultas.generarModeloListaProveedores();
            jListProveedores.setModel(dlm);

            JOptionPane.showMessageDialog(null, "Registro borrado con éxito", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            deshabilitarBotones();
        }
    }
    /**
     * Obtener el id de la cadena de caracteres del elemento seleccionado de la
     * lista
     *
     * @return identificador de empleado
     */
    private Integer extraerId() {
        //Obtener elemento seleccionado de la lista
        String proveedor = jListProveedores.getSelectedValue();
        //Obtener la posición del primer carácter de espacio para la obtención del número de id
        Integer indice = proveedor.indexOf(" ");

        //Cortar la cadena desde su inicio hasta la posición del primer espacio
        String idCadena = proveedor.substring(0, indice);

        //Conversión de la cadena a entero
        Integer id = Integer.parseInt(idCadena);
        return id;
    }

