    /**
     * Ejecución del inicio de sesión. Se comprueban los credenciales
     * introducidos
     */
    private void ejecutarLogin() {
        //Obtención de datos de los campos
        String nombreUsuario = jTextFieldUsuario.getText();
        String contrasenha = jPasswordField1.getText();
        //Comprobación de los campos
        //Si no están vacíos
        if (comprobarCamposVacios(nombreUsuario, contrasenha) == false) {
            //Validación de credenciales
            boolean usuarioValido = comprobarLogin.validarCredenciales(nombreUsuario, contrasenha);
            //Si el usuario es válido
            if (usuarioValido) {
                String rol = obtenerRolEmpleado(nombreUsuario);
                Menu menu = new Menu(nombreUsuario, rol);
                menu.setVisible(true);
                this.setVisible(false);
                //Si el usuario no es válido
            } else {
                JOptionPane.showMessageDialog(null, "El usuario o contraseña no son correctos", "Error de autenticación", ERROR_MESSAGE);
            }
        }
    }

    private String obtenerRolEmpleado(String nombreUsuario) {
        Empleados empleado = consultar.obtenerEmpleado(nombreUsuario);
        String rol = empleado.getRol();
        return rol;
    }

    /**
     * Comprobar si los campos se encuentran vacíos
     *
     * @param nombreUsuario Nombre de usuario introducido por el usuario
     * @param contrasenha Contraseña introducida por el usuario
     */
    private boolean comprobarCamposVacios(String nombreUsuario, String contrasenha) {
        boolean estanVacios = true;
        //Si el nombre de usuario es nulo
        if (nombreUsuario.equals("")) {
            JOptionPane.showMessageDialog(null, "El campo \"Usuario\" no puede estar vacío", "Usuario vacío", ERROR_MESSAGE);
            estanVacios = true;
        } //Si la contraseña es nula
        else if (contrasenha.equals("")) {
            JOptionPane.showMessageDialog(null, "El campo \"Contraseña\" no puede estar vacío", "Contraseña vacía", ERROR_MESSAGE);
            estanVacios = true;
        } else {
            estanVacios = false;
        }
        return estanVacios;
    }

